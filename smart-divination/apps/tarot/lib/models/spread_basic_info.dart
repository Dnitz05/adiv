/// Basic spread information for display in learning screens
///
/// This is a lightweight model that contains just enough information
/// to display a spread card in lists without loading full spread data.
class SpreadBasicInfo {
  const SpreadBasicInfo({
    required this.id,
    required this.name,
    required this.nameCA,
    required this.nameES,
    required this.description,
    required this.descriptionCA,
    required this.descriptionES,
    required this.cardCount,
    required this.complexity,
    required this.category,
  });

  final String id;
  final String name;
  final String nameCA;
  final String nameES;
  final String description;
  final String descriptionCA;
  final String descriptionES;
  final int cardCount;
  final String complexity;
  final String category;

  /// Get localized name
  String getName(String locale) {
    switch (locale) {
      case 'ca':
        return nameCA;
      case 'es':
        return nameES;
      default:
        return name;
    }
  }

  /// Get localized description
  String getDescription(String locale) {
    switch (locale) {
      case 'ca':
        return descriptionCA;
      case 'es':
        return descriptionES;
      default:
        return description;
    }
  }

  /// Get complexity label
  String getComplexityLabel(String locale) {
    switch (complexity) {
      case 'simple':
      case 'beginner':
        switch (locale) {
          case 'ca':
            return 'Principiant';
          case 'es':
            return 'Principiante';
          default:
            return 'Beginner';
        }
      case 'moderate':
      case 'intermediate':
        switch (locale) {
          case 'ca':
            return 'Intermedi';
          case 'es':
            return 'Intermedio';
          default:
            return 'Intermediate';
        }
      case 'complex':
      case 'advanced':
        switch (locale) {
          case 'ca':
            return 'Avançat';
          case 'es':
            return 'Avanzado';
          default:
            return 'Advanced';
        }
      default:
        return complexity;
    }
  }
}

