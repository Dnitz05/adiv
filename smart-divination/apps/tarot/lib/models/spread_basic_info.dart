      (spread) => spread.id == spreadId,
    );
  } catch (e) {
    return null;
  }
}

/// Get spreads for a list of IDs
List<SpreadBasicInfo> getSpreadsForIds(List<String> spreadIds) {
  return spreadIds
      .map((id) => getSpreadBasicInfoById(id))
      .whereType<SpreadBasicInfo>()
      .toList();
}
